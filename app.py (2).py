# =============================================================================
# ETHICAL CONSIDERATIONS
# =============================================================================
#
# 1. FAIRNESS & BIAS
#    The model is trained on historical marketing data. If past campaigns
#    were disproportionately targeted at — or withheld from — certain
#    demographic groups (e.g., customers with lower incomes or certain
#    marital statuses), those biases may be encoded in the model's learned
#    patterns. We regularly audit the feature importance rankings to ensure
#    protected or sensitive attributes are NOT the primary drivers of
#    predictions. If Age or Marital_Status appear in the top 3 features,
#    we investigate and consider removing or transforming those inputs.
#
# 2. TRANSPARENCY & EXPLAINABILITY
#    Gradient Boosting is not inherently human-interpretable (unlike
#    Logistic Regression). To maintain explainability:
#      • We publish the Top 10 Feature Importance chart in the app.
#      • We display the probability score alongside every prediction so
#        analysts understand how confident the model is.
#      • For high-stakes decisions, a SHAP (SHapley Additive exPlanations)
#        analysis can be added to explain individual predictions.
#
# 3. DATA PRIVACY (GDPR / CCPA)
#    Customer data includes income, purchase history, and household
#    demographics — all of which are personally sensitive. Mitigations:
#      • No PII (name, email, address) is used as a model feature.
#      • Data is processed in-session only; Streamlit does not persist
#        uploaded CSVs to a server.
#      • In a production pipeline, data must be anonymized/pseudonymized
#        before ingestion.
#
# 4. INFORMED CONSENT
#    Customers whose data is used to train the model should be informed
#    (via privacy policy) that their purchase behavior influences which
#    marketing campaigns they receive. This aligns with GDPR Article 22
#    on automated decision-making.
#
# =============================================================================


# =============================================================================
# RISKS & MITIGATIONS
# =============================================================================
#
# RISK 1 — MODEL DRIFT
#   Description : Consumer behavior shifts seasonally and economically.
#                 A model trained on 2020–2022 data may perform poorly in 2025.
#   Mitigation  : Schedule quarterly retraining with the latest campaign data.
#                 Track F1-score (class 1) monthly against actual campaign
#                 outcomes. Set an alert threshold (e.g., F1 drops below 0.55)
#                 to trigger an early retraining cycle.
#
# RISK 2 — CLASS IMBALANCE
#   Description : Only ~15% of customers respond (class 1), so the model is
#                 incentivized to predict "No" for everyone and still achieve
#                 ~85% accuracy — which is misleading.
#   Mitigation  : We use stratify=y in train_test_split to preserve class
#                 ratios. We report Recall and F1 for class 1 (not just
#                 accuracy). Further options include SMOTE oversampling or
#                 adjusting class_weight='balanced' in GradientBoostingClassifier.
#
# RISK 3 — OVERRELIANCE ON MODEL OUTPUT
#   Description : Marketing teams may treat model predictions as absolute
#                 truth and stop applying human judgment, leading to missed
#                 opportunities or customer alienation.
#   Mitigation  : Frame the tool as a "prioritization aid," not an oracle.
#                 Display confidence scores prominently so analysts can
#                 apply discretion on borderline cases (e.g., 51% confidence).
#                 Require human sign-off before excluding any customer segment.
#
# RISK 4 — DATA LEAKAGE
#   Description : Features like Total_Spending or Total_Purchases could
#                 inadvertently include post-campaign transactions if the
#                 dataset is not sliced correctly, causing artificially
#                 inflated performance metrics.
#   Mitigation  : Enforce a strict temporal cutoff — only include customer
#                 data recorded *before* the campaign launch date. Validate
#                 the dataset's Dt_Customer range before each training run.
#
# RISK 5 — FEEDBACK LOOP
#   Description : If the model consistently targets the same customer segments,
#                 those segments become the only ones measured for response,
#                 creating a self-reinforcing loop that ignores untested segments.
#   Mitigation  : Randomly include a small holdout group (~5%) in each campaign
#                 regardless of model prediction to continuously gather
#                 unbiased ground-truth data from the full customer population.
#
# =============================================================================


