# OVERVIEW
# --------
# Our Gradient Boosting Classifier (selected as the best-performing model)
# is deployed as an interactive Streamlit web application. The app allows
# marketing teams to:
#   1. Upload new customer CSV data and automatically retrain the model.
#   2. View model performance metrics (Accuracy, Precision, Recall, F1).
#   3. Explore the Top 10 features that drive campaign response.
#   4. Enter individual customer attributes and receive a live
#      "Likely to Respond / Unlikely to Respond" prediction with a
#      confidence probability.
#
# DEPLOYMENT ARCHITECTURE
# -----------------------
#   Phase 1 — Data & Training
#     • User uploads superstore_data.csv via the Streamlit sidebar.
#     • The preprocessing pipeline (null handling, age conversion, tenure
#       calculation, feature engineering, one-hot encoding) runs automatically.
#     • The Gradient Boosting model is trained in-session and cached using
#       @st.cache_data to avoid unnecessary re-training on reruns.
#
#   Phase 2 — Prediction Interface
#     • Marketing analysts enter customer attributes (income, age, recency,
#       spending, household size, etc.) via sliders and input fields.
#     • The trained model returns: predicted class (0/1) + probability score.
#     • A clear visual decision card ("✅ Likely" / "❌ Unlikely") is shown
#       alongside an actionable recommendation for the marketing team.
#
#   Phase 3 — Monitoring & Maintenance
#     • After each campaign, actual response outcomes are compared against
#       predictions to compute real-world F1-score.
#     • The model is scheduled for quarterly retraining as new customer
#       transaction data accumulates.
#     • Model versions are tracked (e.g., with MLflow or DVC) to enable
#       rollback if a new version underperforms.
#
# HOW TO RUN
# ----------
#   pip install streamlit scikit-learn pandas matplotlib
#   streamlit run streamlit_app.py
#
